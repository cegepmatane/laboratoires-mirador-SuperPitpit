package modele;

public class EquipeLigueNationnal {

	protected String nom;
	protected int nombreJoueurSigne;
	protected float masseSalariale;
	protected int nombreCoupeStanley;
	protected int anneesExistance;
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getNombreJoueurSigne() {
		return nombreJoueurSigne;
	}
	public void setNombreJoueurSigne(int nombreJoueurSigne) {
		this.nombreJoueurSigne = nombreJoueurSigne;
	}
	public float getMasseSalariale() {
		return masseSalariale;
	}
	public void setMasseSalariale(float masseSalariale) {
		this.masseSalariale = masseSalariale;
	}
	public int getNombreCoupeStanley() {
		return nombreCoupeStanley;
	}
	public void setNombreCoupeStanley(int nombreCoupeStanley) {
		this.nombreCoupeStanley = nombreCoupeStanley;
	}
	public int getAnneesExistance() {
		return anneesExistance;
	}
	public void setAnneesExistance(int anneesExistance) {
		this.anneesExistance = anneesExistance;
	}
	
}
